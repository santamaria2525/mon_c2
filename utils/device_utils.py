"""
デバイス関連のユーティリティ関数
"""

def get_terminal_number(device_port: str) -> str:
    """ポート番号から端末番号を取得
    
    Args:
        device_port: デバイスのポート（例: "127.0.0.1:62028"）
        
    Returns:
        端末番号（例: "端末4"）
    """
    port_to_terminal = {
        "127.0.0.1:62025": "端末1",
        "127.0.0.1:62026": "端末2", 
        "127.0.0.1:62027": "端末3",
        "127.0.0.1:62028": "端末4",
        "127.0.0.1:62029": "端末5",
        "127.0.0.1:62030": "端末6",
        "127.0.0.1:62031": "端末7",
        "127.0.0.1:62032": "端末8"
    }
    return port_to_terminal.get(device_port, f"端末{device_port.split(':')[-1]}")

def get_terminal_number_only(device_port: str) -> str:
    """ポート番号から端末番号のみを取得（「端末」という文字なし）
    
    Args:
        device_port: デバイスのポート（例: "127.0.0.1:62028"）
        
    Returns:
        端末番号（例: "4"）
    """
    port_to_terminal = {
        "127.0.0.1:62025": "1",
        "127.0.0.1:62026": "2", 
        "127.0.0.1:62027": "3",
        "127.0.0.1:62028": "4",
        "127.0.0.1:62029": "5",
        "127.0.0.1:62030": "6",
        "127.0.0.1:62031": "7",
        "127.0.0.1:62032": "8"
    }
    return port_to_terminal.get(device_port, device_port.split(":")[-1])